import { toast } from "react-toastify";

export const toastSuccess = (frase) => {
    toast.success(frase, {
        position: "top-right",
        autoClose: 2000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "dark",
    });
}

